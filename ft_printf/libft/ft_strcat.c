#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char *cpy;

	cpy = s1;
	while (*cpy != '\0')
		cpy++;
	while (*s2 != '\0')
	{
		*cpy++ = *s2++;
	}
	*cpy = '\0';
	return (s1);
}
